			Mode9_open_ON_ComingCall();
			break;
		case MODEL10_OPEN_SENDSMS:
			Mode10_open_ON_SendSms();
			break;
		case MODEL11_OPEN_COMINGSMS:
			Mode11_open_ON_ComingSms();
			break;
		case MODEL12_OPEN_POWEROFF:
			Mode12_open_ON_PowerOff();
