			Mode3_open_Music();
			break;
		case MODEL4_OPEN_LOCK:
			Mode4_open_ON_Lock();
			break;
		case MODEL5_OPEN_LESSBATTERY:
			Mode5_open_ON_LessBattery();
			break;
		case MODEL6_OPEN_UNREADSMS:
			Mode6_open_ON_UnReadSms();
